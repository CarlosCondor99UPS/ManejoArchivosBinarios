    public String validarApellido(String apellido) {
        while (apellido.length() != 50) {
            apellido = apellido + " ";
        }
        return apellido;
    }

    public String validarNombre(String nombre) {
        while (nombre.length() != 50) {
            nombre = nombre + " ";
        }
        return nombre;
    }

    public boolean validarCedula(String cedula) {
        boolean cedulaCorrecta = false;
        try {
            if (cedula.length() == 10) {
                int tercerDigito = Integer.parseInt(cedula.substring(2, 3));
                if (tercerDigito < 6) {
                    int[] coefValCedula = {2, 1, 2, 1, 2, 1, 2, 1, 2};
                    int verificador = Integer.parseInt(cedula.substring(9, 10));
                    int suma = 0;
                    int digito = 0;
                    for (int i = 0; i < (cedula.length() - 1); i++) {
                        digito = Integer.parseInt(cedula.substring(i, i + 1)) * coefValCedula[i];
                        suma += ((digito % 10) + (digito / 10));
                    }
                    if ((suma % 10 == 0) && (suma % 10 == verificador)) {
                        cedulaCorrecta = true;
                    } else if ((10 - (suma % 10)) == verificador) {
                        cedulaCorrecta = true;
                    } else {
                        cedulaCorrecta = false;
                    }
                } else {
                    cedulaCorrecta = false;
                }
            } else {
                cedulaCorrecta = false;
            }
        } catch (NumberFormatException nfe) {
            cedulaCorrecta = false;
        } catch (Exception err) {
            cedulaCorrecta = false;
        }
        return cedulaCorrecta;
    }
    public boolean validarFecha(String diaS, String mesS, String anoS){
        try {
            int dia=Integer.parseInt(diaS);
            int mes=Integer.parseInt(mesS);
            int ano=Integer.parseInt(anoS);
            if (dia>30){
                JOptionPane.showMessageDialog(this, "Dia incorrecto");
                return false;
            } 
            if (mes>12){
                JOptionPane.showMessageDialog(this, "Mes Incorrecto");
                return false;
            }
            if (ano>2019||ano<1900){
                JOptionPane.showMessageDialog(this, "AÃ±o Incorrecto");
                return false;
            }
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Fecha Incorrecta");
            return false;
        }
        return true;
    }
    
    public boolean validarEdad(int edad){
        if (edad<0||edad>99)
            return false;
        return true;
    }

